import sys

# 第一個版本（遞迴）加上詳細註解

# 遞迴函數：計算爬上 x 階的方法數量
def f(x):
    if x == 0 or x == 1:
        # 基本情況：
        # 如果樓梯是 0 階或 1 階，只有 1 種走法
        return 1
    else:
        # 遞迴關係式：
        # 假設每次只能爬 1 階或 2 階
        # 則 f(x) = f(x-1) + f(x-2)
        # 表示要到達第 x 階，可以從第 x-1 階往上爬一階，
        # 或是從第 x-2 階往上爬兩階，因此總方法數是這兩者的總和
        return f(x - 1) + f(x - 2)

# 從標準輸入讀取資料（可讀多筆）
for s in sys.stdin:
    coin = int(s)       # 將輸入轉換為整數，代表樓梯的階數
    print(f(coin))      # 呼叫遞迴函數 f() 計算結果並輸出
