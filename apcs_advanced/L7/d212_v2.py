import sys

# 第二個版本（動態規劃）加上詳細註解

# 建立一個大小為 100 的陣列來存儲每個階梯數對應的走法數量
f = [0 for i in range(100)]  # 初始化動態規劃陣列，全部先設為 0
f[0] = 1  # 爬 0 階的方法數：只有一種（不動）
f[1] = 1  # 爬 1 階的方法數：只有一種（一步）

# 預先使用動態規劃計算出 0 到 99 階的所有可能走法數
for i in range(2, 100):
    # 遞推公式：f[i] = f[i-1] + f[i-2]
    # 表示爬到第 i 階的方法數 = 從 i-1 階爬一步上來的方法數 + 從 i-2 階爬兩步上來的方法數
    f[i] = f[i - 1] + f[i - 2]

# 從標準輸入讀取資料（可讀多筆）
for s in sys.stdin:
    n = int(s)       # 將輸入轉為整數，代表樓梯的階數
    print(f[n])      # 輸出對應階數的走法數（直接查表）
