# 建立一個字典 `letter`，將特殊的 4 位元碼對應到特定的英文字母
letter = {
    "0 1 0 1": "A",
    "0 1 1 1": "B",
    "0 0 1 0": "C",
    "1 1 0 1": "D",
    "1 0 0 0": "E",
    "1 1 0 0": "F"
}

# 使用無限迴圈來持續讀取輸入
while True:
    try:
        # 讀取一個整數 n，代表接下來有多少組編碼
        n = int(input())
        ans = ""  # 用來儲存解碼後的結果字串

        # 依照 `n` 的數量，逐行讀取輸入並解碼
        for _ in range(n):
            s = input()  # 讀取 4 位元碼 (格式如 "0 1 0 1")
            ans += letter[s]  # 根據字典對應的值，將對應的字母加到 `ans`

        # 輸出解碼後的字串
        print(ans)
    
    except:
        # 當發生異常 (例如 EOF, 輸入結束或格式錯誤) 時，跳出迴圈並結束程式
        break
