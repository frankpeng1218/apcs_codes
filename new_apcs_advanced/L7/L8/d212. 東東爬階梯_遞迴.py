import sys

# 定義一個函式 f(x)，用來計算「爬 x 階樓梯」的方法數量
def f(x):
    if x == 0 or x == 1:
        # 如果樓梯只有 0 階或 1 階，爬法只有一種：
        # x = 0 → 不動 (1 種)
        # x = 1 → 走一步 (1 種)
        return 1
    else:
        # 遞迴公式：
        # 若要爬到第 x 階，可以從 (x-1) 階走一步上來，或是從 (x-2) 階走兩步上來
        # 因此方法數 = f(x-1) + f(x-2)
        return f(x-1) + f(x-2)

# 驗證：f(2) = f(1) + f(0) = 1 + 1 = 2
# 代表爬 2 階的方法有兩種：
#   (1) 走一步 + 一步
#   (2) 直接走兩步

# 從標準輸入讀取資料 (每一行是一個整數 n)
for s in sys.stdin:
    n = int(s)        # 將讀入的字串轉換成整數
    print(f(n))       # 輸出爬 n 階樓梯的方法數
