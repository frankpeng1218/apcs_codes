import sys

# === 前置作業 ===
# 為了避免每次輸入都重新計算 (浪費時間)，
# 先把所有可能的「爬樓梯方法數」都算好並存起來
# 假設最大需要計算到 f(999)，所以先建立大小為 1000 的陣列
f = [0 for i in range(1000)]  # 建立一個長度為 1000 的陣列，全部初始化為 0

# 初始條件：
f[0] = 1  # 爬 0 階的方法 = 1 種 (不動也算一種方法)
f[1] = 1  # 爬 1 階的方法 = 1 種 (只能走一步)

# 利用遞推公式：f[i] = f[i-1] + f[i-2]
# 從 2 開始一路計算到 999，並把結果存進陣列
for i in range(2, 1000):
    f[i] = f[i-1] + f[i-2]


# === 標準輸入處理 ===
# 逐行讀取輸入，每一行輸入一個整數 n
for s in sys.stdin:
    n = int(s)       # 把輸入轉成整數
    print(f[n])      # 直接輸出事先算好的答案
