#
# 二維串列的元素存取與操作
#

# -----------------------------
# 建立 3x3 的二維串列
# 每個元素初始值皆為 0
# -----------------------------
matrix = [[0] * 3 for _ in range(3)]

# 印出初始化後的矩陣
# 輸出結果：
# [[0, 0, 0],
#  [0, 0, 0],
#  [0, 0, 0]]
print("初始化矩陣：", matrix)


# -----------------------------
# 將矩陣「主對角線」上的元素設為 1
# 主對角線條件：列索引 == 行索引
# -----------------------------
for i in range(len(matrix)):   # len(matrix) 代表「列數」
    matrix[i][i] = 1           # (0,0), (1,1), (2,2)

# 印出修改後的矩陣
print("修改後矩陣：", matrix)


# -----------------------------
# 使用巢狀迴圈逐一存取並印出矩陣元素
# -----------------------------
for i in range(len(matrix)):        # 外層迴圈：跑每一列
    for j in range(len(matrix[i])): # 內層迴圈：跑該列的每一個元素
        print(matrix[i][j], end=" ")
    print()  # 每印完一列就換行，讓輸出呈現矩陣形狀
