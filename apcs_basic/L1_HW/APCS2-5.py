a = 8  # 定義變數 a，值為 8
b = 7  # 定義變數 b，值為 7
c = -6  # 定義變數 c，值為 -6
d = 11.11  # 定義變數 d，值為 11.11（浮點數）

result = a + b / 0.3 * c + 9 * (d - 8)  
# 計算結果：
# 1. b / 0.3 = 7 / 0.3 = 23.3333
# 2. 23.3333 * c = 23.3333 * (-6) = -140.0
# 3. 9 * (d - 8) = 9 * (11.11 - 8) = 9 * 3.11 = 27.99
# 4. a + (-140.0) + 27.99 = 8 - 140.0 + 27.99 = -104.01

print("輸出的結果為:" + str(result))  # 轉換 result 為字串並輸出
