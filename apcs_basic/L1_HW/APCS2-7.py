X = 10  # 定義變數 X，值為 10
result = X * (100 - X) * (3 * X + 5)  
# 計算步驟：
# (100 - 10) = 90
# (3 * 10 + 5) = 35
# 10 * 90 * 35 = 31500

print("X=", X, ", result=", result)  # 輸出 X 與計算結果

X = 50  # 將 X 更新為 50
result = X * (100 - X) * (3 * X + 5)  
# 計算步驟：
# (100 - 50) = 50
# (3 * 50 + 5) = 155
# 50 * 50 * 155 = 387500

print("X=", X, ", result=", result)  # 輸出 X 與計算結果

X = 100  # 將 X 更新為 100
result = X * (100 - X) * (3 * X + 5)  
# 計算步驟：
# (100 - 100) = 0
# 任何數乘以 0 都為 0，結果為 0

print("X=", X, ", result=", result)  # 輸出 X 與計算結果
