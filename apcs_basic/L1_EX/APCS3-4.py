#start

s = input("輸入分鐘與秒數:")  # 讓使用者輸入分鐘與秒數，輸入格式為「分鐘 秒數」（以空格分隔）
t = s.split()  # 使用 split() 以空格拆分輸入的字串，轉為串列

sec = int(t[0]) * 60 + int(t[1])  # 轉換為整數後計算總秒數（分鐘轉秒 + 秒數）

print(sec)  # 輸出總秒數

#
# 串列綜合表達式寫法
#
s = list(map(int, input("輸入分鐘與秒數:").split()))  
# 使用 map() 將輸入轉換為整數並存入串列 s

print(s[0] * 60 + s[1])  # 直接使用串列中的值計算總秒數並輸出
