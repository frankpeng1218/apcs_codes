str1 = "星期一的天氣晴, 氣溫"  # 定義變數 str1，內容為字串 "星期一的天氣晴, 氣溫"
temp = 28  # 定義變數 temp，數值為 28（整數）
str3 = "度"  # 定義變數 str3，內容為字串 "度"

print(str1 + str(temp) + str3)  # 透過 + 串接字串，需先將 temp 轉換為字串，結果為 "星期一的天氣晴, 氣溫28度"
print(str1, temp, str3)  # 直接使用 print() 以逗號分隔變數輸出，Python 會自動轉換 temp 為字串，結果相同但輸出時有空格："星期一的天氣晴, 氣溫 28 度"
